#include <stdlib.h>
#include <string.h>

static unsigned long KsStrLen(const STR* s) {
    unsigned long n = 0;
    while (s[n]) n++;
    return n;
}

#define KsMalloc malloc
#define KsFree free

WORD64 WoofsHashString64(PSTR String) {
    unsigned long StrLen = KsStrLen(String);

    WORD64 HashBase = Constants[254];
    for (int i = 0; i < StrLen; i++)
        HashBase ^= (Constants[(String[i] & 0x00FF) ^ ((String[i] & 0xFF00) >> 8)] * i);

    return HashBase;
}
